"""
Задание 2: Ручной счёт ROC-AUC
Классификатор выдал следующие прогнозируемые метки класса и вероятности принадлежности к классу "1". На основе полученных данных рассчитайте метрику ROC_AUC. Тезисно описать ход решения.
Ответ округлить до сотых, например: 4,12
"""

def roc_auc_score(y_true, y_score):
    """
    Вычисляем ROC-AUC по истинным меткам (0/1) и предсказанным вероятностям.
    Используется метод суммы рангов положительных объектов.
    """
    # Соединяем метки и вероятности в пары
    data = list(zip(y_score, y_true))
    # Сортируем по убыванию вероятности
    data.sort(reverse=True, key=lambda x: x[0])
    
    # Количество положительных и отрицательных
    P = sum(y_true)
    N = len(y_true) - P
    
    if P == 0 or N == 0:
        return 0.5  # если только один класс, AUC не определён, но для корректности вернём 0.5
    
    # Ранжируем объекты: ранг = позиция в отсортированном списке (начиная с 1)
    # В случае равных вероятностей нужно усреднять ранги, но в наших данных все вероятности уникальны.
    sum_ranks_pos = 0
    for rank, (_, label) in enumerate(data, start=1):
        if label == 1:
            sum_ranks_pos += rank
    
    # Формула AUC = (S - P*(P+1)/2) / (P * N)
    auc = (sum_ranks_pos - P * (P + 1) / 2) / (P * N)
    return auc

if __name__ == "__main__":
    # Данные из таблицы (истинная метка, вероятность)
    # Воспроизведены в уже отсортированом по убыванию вероятности порядке
    true_labels = [
        1,  # 0.95
        0,  # 0.90
        1,  # 0.85
        0,  # 0.80
        1,  # 0.75
        1,  # 0.70
        1,  # 0.65
        1,  # 0.60
        0,  # 0.55
        0,  # 0.50
        0,  # 0.45
        1,  # 0.40
        0,  # 0.35
        0,  # 0.30
        0,  # 0.25
    ]
    probabilities = [
        0.95, 0.90, 0.85, 0.80, 0.75, 0.70, 0.65, 0.60,
        0.55, 0.50, 0.45, 0.40, 0.35, 0.30, 0.25
    ]
    
    auc = roc_auc_score(true_labels, probabilities)
    # Округление до сотых и замена точки на запятую для требуемого формата
    formatted = f"{auc:.2f}".replace('.', ',')
    print(f"ROC-AUC (расчёт): {formatted}")
