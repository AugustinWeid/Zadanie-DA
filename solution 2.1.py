"""Задание 1: Изоморфизмы
Реализовать функцию (или тело функции), которая проверяет на 
изоморфность два слова. Пояснение: строки s и t называются изоморфными,
если все вхождения каждого символа строки s можно последовательно 
заменить другим символом и получить строку t. Порядок символов при 
этом должен сохраняться, а замена — быть уникальной. Так, два разных
символа строки s нельзя заменить одним и тем же символом из строки t,
а вот одинаковые символы в строке s должны заменяться одним и тем же 
символом.
# Пример:
s = 'paper' 
t = 'title' 
print(is_isomorphic(s, t))
# Вывод: 
True
Оценить оптимальность решения по времени и памяти и прикрепить текст кода.
"""
def is_isomorphic(s: str, t: str) -> bool:
    if len(s) != len(t):
        return False

    map_s_to_t = {}
    map_t_to_s = {}

    for char_s, char_t in zip(s, t):
        # Проверка отображения из s в t
        if char_s in map_s_to_t:
            if map_s_to_t[char_s] != char_t:
                return False
        else:
            # Проверка, что char_t ещё не сопоставлен другому символу из s
            if char_t in map_t_to_s:
                return False
            map_s_to_t[char_s] = char_t
            map_t_to_s[char_t] = char_s

    return True


if __name__ == "__main__":
    # Интерактивный ввод
    s = input("Введите первую строку: ")
    t = input("Введите вторую строку: ")
    result = is_isomorphic(s, t)
    print(f"Строки '{s}' и '{t}' изоморфны? {result}")
